
function toggle() {

	var el = document.getElementById("taskform");

	el.style.display = (el.style.display != 'none' ? 'none' : '' );

}

function hide() {

	var el = document.getElementById("taskform");

	el.style.display = 'none';

}

function show() {

	var el = document.getElementById("taskform");

	el.style.display = 'block';

}